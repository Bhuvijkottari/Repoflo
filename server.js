// gemini-flash-lite-latest: fastest available model (~2s response)
const GEMINI_FAST_URL = `https://generativelanguage.googleapis.com/v1beta/models/gemini-flash-lite-latest:generateContent?key=${GEMINI_KEY}`;
const GEMINI_URL = GEMINI_FAST_URL;

// ── helper: call Gemini with text only ────────────────────────────────────────
async function geminiText(prompt, url = GEMINI_URL, maxTokens = 4096) {
  const res = await fetch(url, {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify({
      contents: [{ parts: [{ text: prompt }] }],
      generationConfig: {
        temperature: 0.1,
        maxOutputTokens: maxTokens,
      },
    }),
  });
  if (!res.ok) {
    const err = await res.text();
    throw new Error(`Gemini error ${res.status}: ${err.substring(0, 100)}`);
  }
  const data = await res.json();
  return data.candidates?.[0]?.content?.parts?.[0]?.text || "";
}

// ── helper: call Gemini with file + text ──────────────────────────────────────
async function geminiMultimodal(fileBase64, mimeType, prompt, url = GEMINI_FAST_URL) {
  const res = await fetch(url, {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify({
      contents: [{
        parts: [
          { inlineData: { mimeType, data: fileBase64 } },
          { text: prompt },
        ],
      }],
      generationConfig: {
        temperature: 0.1,
        maxOutputTokens: 2048,
      },
    }),
  });
  if (!res.ok) {
    const err = await res.text();
    throw new Error(`Gemini error ${res.status}: ${err}`);
  }
  const data = await res.json();
  return data.candidates?.[0]?.content?.parts?.[0]?.text || "";
}

// ── AI markers for GitHub ─────────────────────────────────────────────────────
const AI_TOOL_MARKERS = ["lovable","bolt.new","v0.dev","cursor","windsurf","replit","created with lovable","built with bolt","generated by ai"];
const AI_FILE_PATTERNS = [".cursorrules",".bolt","lovable.config",".v0",".replit"];

async function checkForAIMarkers(username, repoName) {
  try {
    const readmeRes = await fetch(`https://api.github.com/repos/${username}/${repoName}/readme`, {
      headers: githubHeaders,
    });
    if (readmeRes.ok) {
      const d = await readmeRes.json();
      if (d.content) {
        const content = Buffer.from(d.content, "base64").toString("utf8").toLowerCase();
        for (const marker of AI_TOOL_MARKERS) if (content.includes(marker)) return true;
      }
    }
    const contentsRes = await fetch(`https://api.github.com/repos/${username}/${repoName}/contents`, {
      headers: githubHeaders,
    });
    if (contentsRes.ok) {
      const contents = await contentsRes.json();
      if (Array.isArray(contents))
        for (const file of contents)
          for (const pattern of AI_FILE_PATTERNS)
            if (file.name.toLowerCase().includes(pattern)) return true;
    }
  } catch { /* silent */ }
  return false;
}

